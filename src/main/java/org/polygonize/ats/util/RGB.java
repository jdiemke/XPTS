package org.polygonize.ats.util;

public class RGB {

    public float red;
    public float green;
    public float blue;

    public RGB(float red, float green, float blue) {
        this.red = red;
        this.green = green;
        this.blue = blue;
    }

    public RGB() {
        this.red = 0.0f;
        this.green = 0.0f;
        this.blue = 0.0f;
    }
}
